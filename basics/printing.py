# Printing strings

print("hello world")

# Printing numbers

print(4.5)

# Printing multiple things

# Adds space in between with multiple prints.

print("4.5", "Hello", 87, False)

# default behaviour

print("4.5", "Hello", 87, False, end='\n')

# this will not create a new line but rather concatenate the end
# to the next printing statement.

print("4.5", "Hello", 87, False, end='|')

print("hi again")



